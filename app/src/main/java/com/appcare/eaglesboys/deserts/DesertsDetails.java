package com.appcare.eaglesboys.deserts;

public class DesertsDetails {

    private String mDesertsImage;
    private String mDesertsName;
    private String mDesertsPrice;
    private String mDesertsProductType;
    private String mDesertsCategory;

    public String getDesertsImage() {
        return mDesertsImage;
    }

    public void setDesertsImage(String mDesertsImage) {
        this.mDesertsImage = mDesertsImage;
    }

    public String getDesertsName() {
        return mDesertsName;
    }

    public void setDesertsName(String mDesertsName) {
        this.mDesertsName = mDesertsName;
    }

    public String getDesertsPrice() {
        return mDesertsPrice;
    }

    public void setDesertsPrice(String mDesertsPrice) {
        this.mDesertsPrice = mDesertsPrice;
    }

    public String getDesertsProductType() {
        return mDesertsProductType;
    }

    public void setDesertsProductType(String mDesertsProductType) {
        this.mDesertsProductType = mDesertsProductType;
    }

    public String getDesertsCategory() {
        return mDesertsCategory;
    }

    public void setDesertsCategory(String mDesertsCategory) {
        this.mDesertsCategory = mDesertsCategory;
    }
}
