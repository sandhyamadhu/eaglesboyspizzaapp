class DesertsDetails {
    private String DesertsImage;
    private String DesertsName;
    private String DesertsPrice;
    private String DesertsProductType;

    public String getDesertsImage() {
        return DesertsImage;
    }

    public void setDesertsImage(String desertsImage) {
        DesertsImage = desertsImage;
    }

    public String getDesertsName() {
        return DesertsName;
    }

    public void setDesertsName(String desertsName) {
        DesertsName = desertsName;
    }

    public String getDesertsPrice() {
        return DesertsPrice;
    }

    public void setDesertsPrice(String desertsPrice) {
        DesertsPrice = desertsPrice;
    }

    public String getDesertsProductType() {
        return DesertsProductType;
    }

    public void setDesertsProductType(String desertsProductType) {
        DesertsProductType = desertsProductType;
    }

    public String getDesertsCategory() {
        return DesertsCategory;
    }

    public void setDesertsCategory(String desertsCategory) {
        DesertsCategory = desertsCategory;
    }

    private String DesertsCategory;


}
